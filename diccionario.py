# Creamos un diccionario con información de una persona
persona = {
    "nombre": "Raquel ",
    "ciudad": "Lindora",
    "profesion": "Modelo"
}

# Imprimimos el valor de la clave 'profesion'
print("Profesión:", persona["profesion"])

# Actualizamos el valor de la clave 'ciudad'
persona["ciudad"] = "San José"

# Imprimimos el diccionario completo actualizado
print("Información actualizada:", persona)
