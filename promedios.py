# Creamos una lista llamada 'notas' que contiene cinco calificaciones numéricas
notas = [85.5, 90.0, 78.25, 88.75, 92.4]

# Usamos la función 'sum()' para sumar todos los elementos de la lista 
suma = sum(notas)

# Calculamos el promedio dividiendo la suma total entre la cantidad de notas (5)
promedio = suma / 5

# Mostramos el resultado en formato 
print(f"El promedio de todas sus notas es {promedio:.2f}")
